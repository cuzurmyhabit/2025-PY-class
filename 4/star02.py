n = int(input())

if n % 2 == 0:
    n -= 1

for i in range(n):
    if i < n // 2:
        print("*" * (i + 1) + " " * (n - 2 * (i + 1)) + "*" * (i + 1))
        # *     *
        # **   **
        # *** ***


    elif i == n // 2:
        print("*" * n)
        # *******

    else:
        print("*" * (n - i) + " " * (2 * (i - n // 2) - 1) + "*" * (n - i))
        # *** ***
        # **   **
        # *     *