n = int(input()) #7

if n % 2 == 0:
    n -= 1

# 처음
for i in range(0, n // 2 + 1): #7//2 -> 3 + 1 = 4
    print((" " * i) + ("*" * (n - (i * 2))))   # 0 *******
                                             # + 5  *****
                                        # if i = 2   ***
                                        # if i = 3    *

#끝
# -1을 해서 * 두 개 찍히는 거 방지
for i in range(n // 2 - 1, -1, -1):
    print((" " * i) + ("*" * (n - (i * 2))))
